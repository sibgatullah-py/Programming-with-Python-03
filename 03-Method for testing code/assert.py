'''
Assert in Python is a debugging tool that is used to make sure something is true,
If the condition is true then nothing happens.
If the condition is false then Python raises an AssertionError and stops the program.

use assert for :
    To catch bugs early 
    To validate assumptions during development
    Helps to test logic without writing extra code 
    
'''
a = 2
b = 2
assert a == b
print("Assert passed!")

c = 3
d = 4
assert c == d